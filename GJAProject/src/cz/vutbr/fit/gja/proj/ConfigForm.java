    /**
     * Kopiruje hodnoty z formularovych poli do objektu Config
     *
     * @return true pokud prosla validace hodnot a lze ulozit
     */
    private boolean storeValues() {

      try {
        config.setMapCenter(new GPSPoint(Double.parseDouble(this.gpsYField.getText()), Double.parseDouble(this.gpsXField.getText())));
      }
      catch(Exception e) {
          JOptionPane.showMessageDialog(this, "Chybný formát čísla pro GPS souřadnice.", "Chyba",JOptionPane.ERROR_MESSAGE );
          return false;
      }


     

      config.setMapZoom((short)(this.zoomField.getSelectedIndex() + 1));



      return true;
    }


