    /**
     * Ulozeni udaju do souboru. Spusteno po kliknuti na tlacitko OK.
     */
    @Action
    public void btnOKClicked()
    {
        try
        {
            GPSPoint p=new GPSPoint(config.getMapCenter());
            if (! this.storeValues())
              return;
            GPSPoint p2=config.getMapCenter();
            if(p.lat!=p2.lat || p.lng != p2.lng)
            {
                BingMapsStat.clearImageCache();
            }
            
            
            
            config.saveConfig(Config.defaultConfigFile);
            
            modalResult = true;

            this.dispose();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage(),"Chyba",JOptionPane.ERROR_MESSAGE );
        }
    }

